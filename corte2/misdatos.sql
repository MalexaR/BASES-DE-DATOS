CREATE TABLE NOMINA
(
  EMPLEADO    INTEGER,
  AÑO         INTEGER,
  MES         INTEGER,
  FECHA_PAGO  DATE,
  SALARIO     INTEGER,
  DESCUENTOS  INTEGER,
  BONOS       INTEGER
);


CREATE TABLE DEPARTAMENTO
(
  ID      INTEGER,
  NOMBRE  VARCHAR2(50 BYTE)
);


CREATE TABLE MUNICIPIO
(
  ID            INTEGER,
  DEPARTAMENTO  INTEGER,
  NOMBRE        VARCHAR2(50 BYTE)
);


CREATE UNIQUE INDEX DEPARTAMENTO_PK ON DEPARTAMENTO
(ID);


CREATE UNIQUE INDEX MUNICIPIO_PK ON MUNICIPIO
(ID, DEPARTAMENTO);


CREATE UNIQUE INDEX NOMINA_PK ON NOMINA
(EMPLEADO, AÑO, MES);


CREATE TABLE ORGANIZACION
(
  ID            INTEGER                         NOT NULL,
